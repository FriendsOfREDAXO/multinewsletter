	<legend>MultiNewsletter FAQ</legend>
	<p><strong>Frage: Warum ist der Link in der Bestätigungsmail in manchen Mailprogrammen
			nicht als Link aktiviert?</strong></p>
	<p>Der Link wird nur dann immer aktiviert, wenn er in den Einstellungen, bei den
		Übersetzungen unter "Text der Bestätigungsmail" auch als HTML-Link programmiert
		wurde. Bitte deshalb das "a href=..." nicht vergessen!</p>
	<p><strong>Frage: Warum wird die Aktivierungsmail nicht verschickt, die anderen
			Mails aber schon?</strong></p>
	<p>Wenn die Spracheinstellungen eingegeben sind sollte dieser "Fehler" behoben sein.</p>
	<p><strong>Frage: Der Link in der Aktivierungsmail funktioniert nicht. Warum?</strong></p>
	<p>In den Systemeinstellungen muss im Feld "URL der Webseite" die URL inklusive
		http:// und am Ende / eingegeben werden.</p>
	<p><strong>Frage: Meine Aktivierungsmail wird nicht verschickt. Warum?</strong></p>
	<p>Das könnte mehrere Ursachen haben. Ist der PHPMailer korrekt konfiguriert?
		Sind in den Einstellungen die Übersetzungen eingepflegt?</p>
