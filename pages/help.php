	<p>Mit dem MultiNewsletter-Package erweitern Sie Ihr REDAXO-System um ein umfangreiches Tool zur
		Verwaltung von mehreren Newsletter in allen Sprachen Ihres Auftritts. Dabei können Sie
		beliebig viele Benutzer verwalten und in diversen Gruppen organisieren.</p>
	<p>Einige Server, z.B. normale Hostingpakete von 1&1, begrenzen den Versand von Mails. Bei 1&1
		ist es z.B. erlaubt 300 Mails in 5 Minuten zu versenden.
		Erst danach werden neue Mails angenommen. Diese Limitierungen k&ouml;nnen eingestellt werden.</p>
