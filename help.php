<b>MultiNewsletter</b>

<br /><br />
Komfortable Benutzerverwaltung, Benutzerim- und export, personalisierte E-Mails,
setzen von Serverlimits, automatischer Reload der Versandseite bei Unterbruch
des Versands, ein Beispieltemplate und vieles mehr bietet der MultiNewsletter.

Eine Besonderheit gegenüber allen anderen Newsletter Addons ist eine
Einstellungsmöglichkeit für limitierten Mailversand bei Hostern wie 1&1.
Dieses AddOn läuft produktiv z.B. auf http://www.inotec-gmbh.com/ und bewältigt
deutlich über 15000 Newsletter Empfänger trotz der Begrenzung von 300 Mails in
5 Minuten durch den Hoster. Natürlich alles Vollautomatisch.

Bei Fragen bitte in den Forums-Thread für MultiNewsletter einklinken:
http://www.redaxo.org/de/forum/addons-packages-f43/fragen-zu-multinewsletter-in-redaxo-5-t21075.html.

HINWEIS: Bei unerwünschtem Newsletterversand können in Deutschland
Schadenersatzforerungen in mehreren Tausend Euro entstehen. Deshalb: Benutzung
auf eigene Gefahr!